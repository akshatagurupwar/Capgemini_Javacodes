package com.capgi.day5;

public class IntegerMethod {
	
	public static void main(System args[]) {
		int b=10;
	Integer i=new Integer(b);

	 System.out.println("toString(i) = " + Integer.toString(b));

	 System.out.println("toHexString(i) =" + Integer.toHexString(i));
	 System.out.println("toOctalString(i) =" + Integer.toOctalString(i));
	 System.out.println("toBinaryString(i) =" + Integer.toBinaryString(i));

	}
}
